website1 = "https://www.pornhub.com"    # This is a string
website2 = "https://www.linkedin.com"   # This is another string

slice = slice(12,-4)  
# This will slice the string from index 12 to -4 (exclusive)
# The slice object can be used to extract a portion of the string
print(website1[slice])  
print(website2[slice])